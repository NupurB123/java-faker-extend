package com.github.javafaker.pharma;

public class StabilityChamber {
	private String temperatureRange;
	private String humidityRange;
	private String lightControl;
	private String chamberSize;

	public String getTemperatureRange() {
		return temperatureRange;
	}

	public void setTemperatureRange(String temperatureRange) {
		this.temperatureRange = temperatureRange;
	}

	public String getHumidityRange() {
		return humidityRange;
	}

	public void setHumidityRange(String humidityRange) {
		this.humidityRange = humidityRange;
	}

	public String getLightControl() {
		return lightControl;
	}

	public void setLightControl(String lightControl) {
		this.lightControl = lightControl;
	}

	public String getChamberSize() {
		return chamberSize;
	}

	public void setChamberSize(String chamberSize) {
		this.chamberSize = chamberSize;
	}

}
