package com.github.javafaker.pharma;

public class FriabilityTester {
	private double testRange;
	private double drumSize;
	private double timerRange;
	private String automationLevel;
	private int sampleCapacity;
	private String operationalStatus;

	public double getTestRange() {
		return testRange;
	}

	public void setTestRange(double testRange) {
		this.testRange = testRange;
	}

	public double getDrumSize() {
		return drumSize;
	}

	public void setDrumSize(double drumSize) {
		this.drumSize = drumSize;
	}

	public double getTimerRange() {
		return timerRange;
	}

	public void setTimerRange(double timerRange) {
		this.timerRange = timerRange;
	}

	public String getAutomationLevel() {
		return automationLevel;
	}

	public void setAutomationLevel(String automationLevel) {
		this.automationLevel = automationLevel;
	}

	public int getSampleCapacity() {
		return sampleCapacity;
	}

	public void setSampleCapacity(int sampleCapacity) {
		this.sampleCapacity = sampleCapacity;
	}

	public String getOperationalStatus() {
		return operationalStatus;
	}

	public void setOperationalStatus(String operationalStatus) {
		this.operationalStatus = operationalStatus;
	}

}
