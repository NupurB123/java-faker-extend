package com.github.javafaker.pharma;

public class MillingMachine {
	private Integer millingCapacity;

	public MillingMachine(Integer millingCapacity) {
		super();
		this.millingCapacity = millingCapacity;
	}

	public MillingMachine() {
		super();
	}

	public Integer getMillingCapacity() {
		return millingCapacity;
	}

	public void setMillingCapacity(Integer millingCapacity) {
		this.millingCapacity = millingCapacity;
	}

}
