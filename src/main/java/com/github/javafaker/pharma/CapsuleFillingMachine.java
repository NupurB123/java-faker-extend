package com.github.javafaker.pharma;

public class CapsuleFillingMachine {
	private String fillingAccuracy;
	private String dosageForms;
	private int productionCapacity;

	public String getFillingAccuracy() {
		return fillingAccuracy;
	}

	public void setFillingAccuracy(String fillingAccuracy) {
		this.fillingAccuracy = fillingAccuracy;
	}

	public String getDosageForms() {
		return dosageForms;
	}

	public void setDosageForms(String dosageForms) {
		this.dosageForms = dosageForms;
	}

	public int getProductionCapacity() {
		return productionCapacity;
	}

	public void setProductionCapacity(int productionCapacity) {
		this.productionCapacity = productionCapacity;
	}

}
