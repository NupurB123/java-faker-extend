package com.github.javafaker.pharma;

public class Microscope {
	private String microscopeType;
	private String magnificationRange;

	public String getMicroscopeType() {
		return microscopeType;
	}

	public void setMicroscopeType(String microscopeType) {
		this.microscopeType = microscopeType;
	}

	public String getMagnificationRange() {
		return magnificationRange;
	}

	public void setMagnificationRange(String magnificationRange) {
		this.magnificationRange = magnificationRange;
	}

}
