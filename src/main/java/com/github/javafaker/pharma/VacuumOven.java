package com.github.javafaker.pharma;

public class VacuumOven {
	private String temperatureRange;
	private String vacuumLevel;
	private double chamberVolume;
	private String heatingOptions;

	public String getTemperatureRange() {
		return temperatureRange;
	}

	public void setTemperatureRange(String temperatureRange) {
		this.temperatureRange = temperatureRange;
	}

	public String getVacuumLevel() {
		return vacuumLevel;
	}

	public void setVacuumLevel(String vacuumLevel) {
		this.vacuumLevel = vacuumLevel;
	}

	public double getChamberVolume() {
		return chamberVolume;
	}

	public void setChamberVolume(double chamberVolume) {
		this.chamberVolume = chamberVolume;
	}

	public String getHeatingOptions() {
		return heatingOptions;
	}

	public void setHeatingOptions(String heatingOptions) {
		this.heatingOptions = heatingOptions;
	}

}
